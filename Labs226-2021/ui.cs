class Product
{
    public string name;
    public int count;
    public double price;
    public string manufacturer;
    public int articulus;

    public Product(string name, int count, double price, string manufacturer, int articulus)
    {
        this.name = name;
        this.count = count;
        this.price = price;
        this.manufacturer = manufacturer;
        this.articulus = articulus;
    }
}
